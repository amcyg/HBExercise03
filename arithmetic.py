def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1*num2

def divide(num1, num2):
    return num1/num2

#to use, type square num1
def square(num1):
    return num1*num1

#to use, type cube num1
def cube(num1):
    return num1*num1*num1

#to use, type pow num1 num2
def power(num1, num2):
    return num1**num2
#to use, type mod num1 num2
def mod(num1, num2):
    return num1%num2
